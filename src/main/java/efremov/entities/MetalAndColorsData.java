package efremov.entities;

public class MetalAndColorsData {

    public String oddNumber;
    public String evenNumber;
    public String element;
    public String color;
    public String metal;
    public String vegetable;

    public MetalAndColorsData(String oddNumber, String evenNumber, String element, String color, String metal, String vegetable) {
        this.oddNumber = oddNumber;
        this.evenNumber = evenNumber;
        this.element = element;
        this.color = color;
        this.metal = metal;
        this.vegetable = vegetable;
    }
}
